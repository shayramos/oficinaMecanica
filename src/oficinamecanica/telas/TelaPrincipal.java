    private void bSalvarEFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarEFActionPerformed
        try{
            String nome = tfNomeEF.getText();
            long cpf = Long.parseLong(tfCpfEF.getText());
            Data datn = new Data(Integer.parseInt(tfDiaEF.getText()),Integer.parseInt(tfMesEF.getText()),Integer.parseInt(tfAnoEF.getText()));
            double sal = Double.parseDouble(tfSalarioEF.getText());
            boolean bonus;
            double salCalc;
            String calc="";
            if(bttSimEF.isSelected()){
                bonus=true;
            }else{
                bonus=false;
            }
            
            switch (sFunIndexArr){
                case 1:
                    sys.getGerenteByRef(sFunIndex).setNome(nome);
                    sys.getGerenteByRef(sFunIndex).setCpf(cpf);
                    sys.getGerenteByRef(sFunIndex).setDatNasc(datn);
                    sys.getGerenteByRef(sFunIndex).setSalario(sal);
                    sys.getGerenteByRef(sFunIndex).setIsBonusable(bonus);
                    salCalc=sys.getGerenteByRef(sFunIndex).calcSalario(sys.BONUS);
                    if(bonus){
                        calc="\nSalario: R$"+String.format( "%.2f", sal)+"\nBonus: R$"+String.format( "%.2f", sys.BONUS*4)+"\nSalario calculado(Salario + Bonus): R$"+String.format( "%.2f", salCalc);
                    }
                    break;
                case 2:
                    sys.getMecanicoByRef(sFunIndex).setNome(nome);
                    sys.getMecanicoByRef(sFunIndex).setCpf(cpf);
                    sys.getMecanicoByRef(sFunIndex).setDatNasc(datn);
                    sys.getMecanicoByRef(sFunIndex).setSalario(sal);
                    sys.getMecanicoByRef(sFunIndex).setIsBonusable(bonus);
                    salCalc=sys.getMecanicoByRef(sFunIndex).calcSalario(sys.BONUS, sys.TAXACOMISSAO);
                    int ns = sys.getMecanicoByRef(sFunIndex).getNServicos();
                    if(bonus){
                        calc="\nSalario: R$"+String.format( "%.2f", sal)+"\nBonus: R$"+String.format( "%.2f", sys.BONUS)+"\nNumero de servicos: "+ns+"\nComissao por servico: R$"+String.format("%.2f", sys.TAXACOMISSAO)+"\nSalario calculado(Salario + Bonus + Comissao): R$"+String.format( "%.2f", salCalc);
                    }else{
                        calc="\nSalario: R$"+String.format( "%.2f", sal)+"\nNumero de servicos: "+ns+"\nComissao por servico: R$"+String.format("%.2f", sys.TAXACOMISSAO)+"\nSalario calculado(Salario + Comissao): R$"+String.format( "%.2f", salCalc);
                    }
                    break;
                case 3:
                    sys.getVendedorByRef(sFunIndex).setNome(nome);
                    sys.getVendedorByRef(sFunIndex).setCpf(cpf);
                    sys.getVendedorByRef(sFunIndex).setDatNasc(datn);
                    sys.getVendedorByRef(sFunIndex).setSalario(sal);
                    sys.getVendedorByRef(sFunIndex).setIsBonusable(bonus);
                    sys.getVendedorByRef(sFunIndex).calcSalario(sys.BONUS, sys.VALORHORAEXTRA);
                    salCalc=sys.getVendedorByRef(sFunIndex).calcSalario(sys.BONUS, sys.VALORHORAEXTRA);
                    int hr = sys.getVendedorByRef(sFunIndex).getHrExtra();
                    if(bonus){
                        calc="\nSalario: R$"+String.format( "%.2f", sal)+"\nBonus: R$"+String.format( "%.2f", sys.BONUS)+"\nHoras extra: "+hr+"\nBonus por hora extra: R$"+String.format("%.2f", sys.VALORHORAEXTRA)+"\nSalario calculado(Salario + Bonus + Horas Extra): R$"+String.format( "%.2f", salCalc);
                    }else{
                        calc="\nSalario: R$"+String.format( "%.2f", sal)+"\nHoras extra: "+hr+"\nBonus por hora extra: R$"+String.format("%.2f", sys.VALORHORAEXTRA)+"\nSalario calculado(Salario + Horas Extra): R$"+String.format( "%.2f", salCalc);
                    }
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Algo de errado não esta certo.");
                    return;
            }
             JOptionPane.showMessageDialog(null, calc+"\n\nDados atualizados com sucesso.");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Dados invalidos.");
            return;
        }
        
       
    }//GEN-LAST:event_bSalvarEFActionPerformed

