    private void bConfirmarOSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmarOSActionPerformed
        int cref=-1;
        try{
            cref = sys.buscaCliente(Long.parseLong(tfCpfOS.getText()));
            if(cref==-1){
                JOptionPane.showMessageDialog(null, "Cliente não encontrado");
                return;
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "CPF invalido.");
        }
        
        
        try{
            sys.getArrOrdemServicos().add(new OrdemServico(sys.codOS, sys.getClienByRef(cref), sys.getClienByRef(cref).buscaCarro((String)cbCarrosOS.getSelectedItem()), new Data(Integer.parseInt(tfDiaOS.getText()), Integer.parseInt(tfMesOS.getText()), Integer.parseInt(tfAnoOS.getText())), sMec));
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Dados invalidos.");
        }
        
        ArrayList<String> arrServs = new ArrayList<>();
        
        for(int i=0;i<lisServicosInOS.getModel().getSize();i++){
            arrServs.add(lisServicosInOS.getModel().getElementAt(i));
        }
        
        for(int j=0;j<arrServs.size();j++){
            sys.getOrdemServicoByRef(sys.buscaOrdemServico(sys.codOS)).addItem(sys.getServicoByRef(sys.buscarServico(arrServs.get(j))));
        }
        
        sys.getOrdemServicoByRef(sys.buscaOrdemServico(sys.codOS)).calcValorTotal();
        
        JOptionPane.showMessageDialog(null, "Servico realizado com sucesso:\n"+sys.getOrdemServicoByRef(sys.buscaOrdemServico(sys.codOS)).toString());
        sys.codOS++;
        sys.getArrMecanicos().get(sys.buscaMecanico(sMec.getCpf())).addServico();
        
    }//GEN-LAST:event_bConfirmarOSActionPerformed

