    public void login(){
        boolean auth=false;
        try{
            Funcionario F = sys.buscaFuncionario(Long.parseLong(tfCpfL.getText()));
            if(F!=null){
                if(F.getSenha()==Integer.parseInt(tfSenhaL.getText())){
                    auth=true;
                }else{
                    JOptionPane.showMessageDialog(null, "CPF ou senha incorretos.");
                    return;
                }
                if(auth){
                    JOptionPane.showMessageDialog(null, "Bem vindo, "+F.getNome()+".");
                    pLogin.setEnabled(false);
                    pLogin.setVisible(false);
                    miDesconectarJF.setEnabled(true);
                    if(F instanceof Gerente){
                        menuGerenciarJF.setEnabled(true);
                        menuServicosJF.setEnabled(true);
                        menuVendasJF.setEnabled(true);
                        sGer=(Gerente)F;
                    }else if(F instanceof Mecanico){
                        menuServicosJF.setEnabled(true);
                        sMec=(Mecanico)F;
                    }else if(F instanceof Vendedor){
                        menuVendasJF.setEnabled(true);
                        sVen=(Vendedor)F;
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, "CPF ou senha incorretos.");
                return;
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Dados invalidos.");
            return;       
        }
    }
