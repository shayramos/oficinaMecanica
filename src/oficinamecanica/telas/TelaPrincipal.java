    private void bConfirmarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmarVActionPerformed
        int cref=-1;
        try{
            cref = sys.buscaCliente(Long.parseLong(tfCpfOS.getText()));
            if(cref==-1){
                JOptionPane.showMessageDialog(null, "Cliente não encontrado");
                return;
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "CPF invalido.");
        }
        
        
        try{
            sys.getArrVendas().add(new Venda(sys.codV, sys.getClienByRef(cref), Integer.parseInt(tfDiaV.getText()), Integer.parseInt(tfMesV.getText()), Integer.parseInt(tfAnoV.getText()),sVen));
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Dados invalidos.");
        }
        
        
        ArrayList<String> arrPecs = new ArrayList<>();
        
        for(int i=0;i<lisPecasInV.getModel().getSize();i++){
            arrPecs.add(lisPecasInV.getModel().getElementAt(i));
        }
        
        for(int j=0;j<arrPecs.size();j++){
            sys.getVendaByRef(sys.buscaVendas(sys.codV)).addItem(sys.getPecaByRef(sys.buscarPeca(arrPecs.get(j))));
            
        }
        
        sys.getVendaByRef(sys.buscaVendas(sys.codV)).calcValorTotal();
        
        JOptionPane.showMessageDialog(null, "Venda realizada com sucesso:\n"+sys.getVendaByRef(sys.buscaVendas(sys.codV)).toString());
        sys.codV++;
        
    }//GEN-LAST:event_bConfirmarVActionPerformed

